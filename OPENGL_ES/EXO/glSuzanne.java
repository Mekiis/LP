	private ByteBuffer vertexBuffer;
	private ByteBuffer colorBuffer;
	private ByteBuffer normalBuffer;
	private ByteBuffer indexBuffer;
	
	public glSuzanne()
	{
		vertexBuffer = ByteBuffer.allocateDirect(vertices.length * 4); // 4 : pas top, dépendant de l'OS ?
		vertexBuffer.order(ByteOrder.nativeOrder());
		indexBuffer = ByteBuffer.allocateDirect(indices.length * 2); // Idem au dessus
		indexBuffer.order(ByteOrder.nativeOrder());
		colorBuffer = ByteBuffer.allocateDirect(colors.length * 4);
		colorBuffer.order(ByteOrder.nativeOrder());
		normalBuffer = ByteBuffer.allocateDirect(normals.length * 4);
		normalBuffer.order(ByteOrder.nativeOrder());
		
		// Coordinates transfer
		vertexBuffer.asFloatBuffer().put(vertices);
		vertexBuffer.position(0);
		
		// Color transfer
		colorBuffer.asFloatBuffer().put(colors);
		colorBuffer.position(0);
		
		// Indices transfer		
		indexBuffer.asShortBuffer().put(indices);
		indexBuffer.position(0);
		
		// Normals buffer
		normalBuffer.asFloatBuffer().put(normals);
		normalBuffer.position(0);
				
	}
	
	public void draw(GL10 gl)
	{
						
		// Pour chaque objet :
		// Materiaux et lumières		
		
		
		gl.glEnable(GL10.GL_LIGHTING);
		
						
		gl.glMaterialfv(GL10.GL_FRONT_AND_BACK, 
                        GL10.GL_DIFFUSE, 
                        FloatBuffer.wrap(mat_diffuse));
		gl.glMaterialfv(GL10.GL_FRONT_AND_BACK, 
                        GL10.GL_SPECULAR, 
                        FloatBuffer.wrap(mat_specular));
		gl.glMaterialfv(GL10.GL_FRONT_AND_BACK, 
                        GL10.GL_SHININESS, 
                        FloatBuffer.wrap(mat_shininess));
		float light_position[] = { 0.0f,0.0f, 50.0f, 1.0f };
		gl.glLightfv(GL10.GL_LIGHT0, GL10.GL_POSITION, FloatBuffer.wrap(light_position));

		gl.glEnable(GL10.GL_LIGHT0);
		
		// Enabled the vertices buffer for writing and to be used during
		// rendering.
		gl.glEnableClientState(GL10.GL_VERTEX_ARRAY);// OpenGL docs.
		gl.glVertexPointer(3, GL10.GL_FLOAT, 0, vertexBuffer);
//		gl.glVertexPointer(3, GL10.GL_FLOAT, 0, FloatBuffer.wrap(vertices));
		
		/*gl.glEnableClientState(GL10.GL_COLOR_ARRAY);
		gl.glColorPointer(4, GL10.GL_FLOAT, 0, colorBuffer);*/
		
		gl.glEnableClientState(GL10.GL_NORMAL_ARRAY);
		gl.glNormalPointer(GL10.GL_FLOAT, 0, normalBuffer);

		gl.glDrawElements(GL10.GL_TRIANGLES, indices.length,// OpenGL docs
				  GL10.GL_UNSIGNED_SHORT, indexBuffer);

		gl.glDisableClientState(GL10.GL_VERTEX_ARRAY); // OpenGL docs
		gl.glDisable(GL10.GL_LIGHTING);
		//gl.glDisableClientState(GL10.GL_COLOR_ARRAY);
	}
